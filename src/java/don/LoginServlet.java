    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String role = request.getParameter("role");

        Connection conn = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/appointment_system", "root", "admin");

            String sql = "";
            switch (role) {
                case "student":
                    sql = "SELECT id, username, 'student' as role FROM students WHERE username = ? AND password = ?";
                    break;
                case "lecturer":
                    sql = "SELECT id, username, 'lecturer' as role FROM lecturers WHERE username = ? AND password = ?";
                    break;
                case "admin":
                    sql = "SELECT id, username, 'admin' as role FROM admin WHERE username = ? AND password = ?";
                    break;
                default:
                    response.sendRedirect("login.jsp?error=Invalid role");
                    return;
            }

            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, username);
            pstmt.setString(2, password);
            rs = pstmt.executeQuery();

            if (rs.next()) {
                int userId = rs.getInt("id");
                String dbUsername = rs.getString("username");
                String dbRole = rs.getString("role");

                HttpSession session = request.getSession();
                session.setAttribute("id", userId);
                session.setAttribute("username", dbUsername);
                session.setAttribute("role", dbRole);

                // Redirect based on role
                switch (dbRole) {
                    case "student":
                        response.sendRedirect("student.jsp");
                        break;
                    case "lecturer":
                        response.sendRedirect("Lecturer.jsp");
                        break;
                    case "admin":
                        response.sendRedirect("admin.jsp");
                        break;
                    default:
                        response.sendRedirect("login.jsp?error=Invalid role");
                        break;
                }
            } else {
                response.sendRedirect("login.jsp?error=Invalid username, password, or role");
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

